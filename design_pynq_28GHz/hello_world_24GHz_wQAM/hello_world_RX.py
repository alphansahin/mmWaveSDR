def binToChar(bitsRX: list[int]) -> str:
    
    bitsPerCharacter = 8
    message = ''
    messageBits = ''.join(list(map(str,bitsRX)))

    for i in range(0,len(bitsRX),bitsPerCharacter):
        char = messageBits[(i):(i+bitsPerCharacter)]
        message += chr(int(char,2))
    return message

